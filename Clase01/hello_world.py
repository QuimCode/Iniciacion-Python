# Tipos de Datos 
# En Python, los tipos de datos son las diferentes categorías de datos que se pueden almacenar y manipular en un programa.
numberVariable = 10 # Variable de tipo entero (int)
floatVariable = 10.5 # Variable de tipo flotante (float)
stringVariable = "Hola Mundo" # Variable de tipo cadena (str)
booleanVariable = True # Variable de tipo booleano (bool)

#=========================================================================================================================#

# Variables de tipo colección
# Estas variables son colecciones de datos, como listas, tuplas, diccionarios y conjuntos.
listVariable = [1, 2, 3, 4] # Variable de tipo lista (list), una lista es una colección ordenada y mutable de elementos.
tupleVariable = (1, 2, 3, 4) # Variable de tipo tupla (tuple), una tupla es una colección ordenada e inmutable de elementos.
dictVariable = {"key": "value"} # Variable de tipo diccionario (dict), un diccionario es una colección no ordenada de pares clave-valor.
setVariable = {4, 2, 3, 1} # Variable de tipo conjunto (set), un conjunto es una colección no ordenada de elementos únicos.

